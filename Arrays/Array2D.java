package Arrays;

public class Array2D {

	public static void main(String[] args) {
		int k;
		int [][] n = { 
			{1, 2, 3, 4},
			{5, 6,  8},
			{9, 10}
		};
	for (int i = 0;i<n.length;i++ ) {
		for(k=0;k<n[i].length;k++) {
			System.out.print(n[i][k]+" ");
		
		}

		
		System.out.println();
	}
		

	}

}
